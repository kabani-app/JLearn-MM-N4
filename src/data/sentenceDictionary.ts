export function getUniqueSentence(kanji: string, meaning: string, pos: string, index: number): DictionarySentence {
  const cleanKanji = kanji.replace(/[~〜()（）]/g, '').trim();
  
  // 1. Direct hand-crafted dictionary lookup
  if (HAND_CRAFTED_SENTENCES[cleanKanji]) {
    return HAND_CRAFTED_SENTENCES[cleanKanji];
  }
  
  // 2. Pos-aware smart variation based on index/hash
  let patterns = NOUN_PATTERNS;
  if (pos === 'Verb') {
    patterns = VERB_PATTERNS;
  } else if (pos === 'Adjective') {
    patterns = ADJ_PATTERNS;
  } else if (pos === 'Adverb') {
    patterns = ADVERB_PATTERNS;
  }
  
  const patternIndex = index % patterns.length;
  const pattern = patterns[patternIndex];
  
  // Safely replace the placeholder
  const ja = pattern.ja.replace("WORD", cleanKanji);
  const mm = pattern.mm.replace("WORD", `${meaning}`);
  
  return { ja, mm };
}
